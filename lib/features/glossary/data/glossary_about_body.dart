/// Long-form brand story text for the TIFFANI Care Guide About surface.
///
/// Single source of truth used both by:
///   * the inline `CareGuideAboutPanel` on `/glossary` (curated excerpt), and
///   * the full reading destination at `/glossary/about`.
///
/// Paragraphs are separated by blank lines (`\n\n`).
const String kGlossaryAboutBody = '''
История Тиффани началась в 2019 году, когда ни в одном из офлайн-магазинов Приднестровья не была представлена на тот момент оригинальная корейская косметика.

Так, мы стали первыми в регионе, кто позаботился о здоровье кожи наших покупательниц, поставляя самую лучшую, отборную косметику напрямую из Южной Кореи (г. Сеул).

С самого начала нашей работы мы были и есть категорически против реализации китайских копий под видом оригинальной продукции. Мы не поддерживаем такую деятельность и ведём борьбу с подобным обманом покупателей. На наших полках представлена только подлинная косметика от самых известных и проверенных брендов Кореи, Европы и США.

С тех пор мало что изменилось, кроме одного — мы безусловно зарекомендовали себя как самая крупная сеть в области корейской бьюти-индустрии в Приднестровье, с безупречной репутацией, качественными консультациями по подбору ухода и миссией, которую выполняем и по сей день.

Это не просто бизнес. И это гораздо больше, чем просто «корейская косметика». Ведь миссия нашего бренда — сделать женщину увереннее в себе с помощью качественного, профессионального ухода в домашних условиях, который будет доступен для всех и точно не нанесёт никакого вреда вашей коже.

Наша задача — учить женщин уходу, чтобы с его помощью обретать гармонию и любовь к себе в каждом прожитом дне. Наслаждаться собой.

Будьте уверены, выбирая Тиффани, вы сделаете лучший из всех возможных выборов и обязательно увидите результат. Сияющие, здоровые лица наших постоянных покупателей, а также более 3000 отзывов в сети — лучшее тому подтверждение.

Во благо. Ценим и благодарим каждую из вас за любовь и внимание к нашему бренду.

С уважением, команда Тиффани.
''';

/// Returns the full brand body split into trimmed, non-empty paragraphs.
List<String> glossaryAboutParagraphs() {
  return kGlossaryAboutBody
      .trim()
      .split('\n\n')
      .map((p) => p.trim())
      .where((p) => p.isNotEmpty)
      .toList(growable: false);
}
